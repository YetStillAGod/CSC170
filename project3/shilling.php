<h3>Continued development (2017–present)
</h3>
<p>
Paradox announced the opening of its third internal studio, Paradox Thalassic located in Malmö, Sweden, in May 2017. The studio was established to develop mobile games based on Paradox's properties.[17] Paradox Interactive's financial performance for 2017 saw a 24% year-on-year increase in revenues to 813.8 million kr, and a 10% year-on-year increase in profits to 339.8 million kr.[18]
Wester stepped down as CEO in August 2018, but he remained executive chairman of the board while board member Ebba Ljungerud took his place as CEO. The move was billed as giving Wester more ability to look for growth opportunities while Ljungerud handled the day-to-day operations of the company, which had since grown to about 300 employees since its foundation.[19][20]
<br /><br />
In March 2019, Paradox announced the opening of Paradox Tectonic, located in Berkeley, California, with Rod Humbleserving as studio lead.[21] Another new studio, Paradox Tinto, was opened in June 2020 in Barcelona, Spain, led by Johan Andersson to oversee Europa Universalis IV development and other Paradox grand strategy titles.[22]
<br /><br />
In June 2020, Paradox became one of the first major publishers to announce support for unionization efforts when it concluded a labour agreement with its employees in its Swedish divisions and the Swedish unions Swedish Confederation of Professional Associations and Unionen.[23][24] In 2021, Paradox committed to overhauling its online forums, due to challenges in engaging its community.[25]
<br /><br />
On 1 September 2021, Ljungerud resigned as CEO "due to differing views on the company's strategy going forward," with the then executive chairman of the board and former CEO Fredrik Wester taking her place and resigning from his position on the board. Following this, Håkan Sjunnesson, at the time deputy chairman of the board, became chairman.[26] At the end of September 2021, the company announced it was cancelling development on several unannounced products to focus on "proven game niches", leaving 15 projects in development.[27] According to Wester, the company's focus remains on their strategy and simulation games, and that they have "sharpened our pipeline further to ensure that the projects with the highest potential have the resources necessary for the best possible development".[27]
<br /><br />
That same September, Swedish publication Breakit reported that an internal survey from Paradox employees found 44% of the 133 responds had reported some type of "mistreatment" in the company, and that many respondents believed there was a "culture of silence" at the firm.[28] The following month, a report from Svenska Dagbladet further investigated this situation, finding that the female employees believed the company was "clearly male-dominated", and with several men in senior management positions in roles involved in harassment and mistreatment of employees. Eurogamer also spoke to Paradox employees, confirming these findings. Paradox replied to these reports that while the prior survey was too small a fraction of their total employee count to take action on, they have hired an independent auditor to review their company culture, starting with their Sweden operations.[29] In February 2022 published the audit on its webpage.[30]
<br /><br />
The company launched Paradox Arc in August 2022 as a new publishing label aimed for games from smaller studios.[31]
</p>

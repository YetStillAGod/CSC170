<h3>Board games
</h3>
<p>At the 2018 PDXCon, Paradox announced it was working with board game designers and publishers to produce a number of board games based on their video game properties, including Europa Universalis, Crusader Kings, Hearts of Iron, and Cities: Skylines.[33]
During PDXCON Remixed, Paradox announced a kickstarter of a Prison Architect board game, designed by Noralie Lubbers and David Turczi.


<h3>Going public (2016)
</h3>
<p>
In March 2016, CEO Fredrik Wester stated in an interview with Di Digital that Paradox Interactive has launched the IPOprocess. The company stated their intention to complete this process within a year, with the goal of spreading ownership between employees and players of their games and "looking for long-term owners who want to take part in the Paradox journey".[13]
On 31 May 2016, trading in Paradox Interactive commenced on Nasdaq First North under the ticker PDX.[14] The initial price offering was 33 per share kr (US$3.96)[15] valuing the company at 3,485 million kr (US$420 million). Paradox set aside about 5% of the shares to allocate to Tencent, valued at about 174.24 million kr (US$21 million).[16] Wester continued to hold 33.3% of the shares of the company, while investment firm Spiltan held to 30.5% of the shares.[16] Finances created by the offering allowed Paradox to begin several acquisitions of various studios and intellectual properties.[16]

</p>

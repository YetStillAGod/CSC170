            <h2> History
</h2>
<h3>Separation from Paradox Entertainment (1999–2004)
</h3>
             <p>Paradox Interactive's origins started with Target Games, a Sweden-based board game company. Target had been producing board and tabletop role-playing games in the 1980s and 1990s, and ventured into video games. By the late 1990s, Target was struggling financially, and they ultimately folded into bankruptcy by 1999. The video game division spun off into a separate entity, Paradox Entertainment, which published video game adaptions of Target's games.[3] Between 2000 and 2003, Paradox Entertainment released the first titles of several grand strategy games, including Europa Universalis, Hearts of Iron, Victoria: An Empire Under the Sun and Crusader Kings. The company had several mediocre releases, including Gettysburg: Armored Warfare, an attempt by the studio to make a massively multiplayer online game on a triple-A scale, but which was released with numerous problems and was critically panned, costing several jobs by the developer.[4]
<br /><br />
By around 2003, Paradox Entertainment began buying the intellectual property rights to various franchises like Conan the Barbarian from Robert E. Howard and Solomon Kane. Fredrik Wester, current CEO of Paradox Interactive, stated that around 2003 he had been brought aboard by Paradox Entertainment to help write their business plan, which included the drive to transform their video game division into a triple-A studio. Wester cautioned them about this, pointing back to the failure of Gettysburg. The Paradox Entertainment executives did not take this advice well and decided it would be better to shutter the video games division and focus exclusively on licensing their acquired brands.[4] Instead, Wester, along with the Paradox Entertainment CEO Theodore Bergquist, bought out the video games division retaining the Swedish Paradox Development Studio and all seven of its developers including Johan Andersson. They gained all intellectual property rights to the studio's past games, forming Paradox Interactive in 2004.[3][4]
</p>
</section>
<section>
<h3>Growth as a publisher (2005–2015)
</h3>
<p>One of the first titles that was planned by Paradox Interactive was Crusader Kings, another grand strategy title. Their publisher, Strategy First, filed for bankruptcy about two months into its release, costing Paradox revenues from those sales as well as the lack of a North American distributor. After the launch of the digital storefront Steam around 2003, Wester experimented with digital marketplaces by offering downloadable content for Victoria via their website. The experiment proved successful, and subsequently in 2006 the company launched Paradox On Demand, a digital storefront with several of Paradox's back-catalog for sale.[4] This eventually was renamed as GamersGate later in 2006. To help support it, Paradox looked to sign on games from developers as to bolster the company's reputation as a world-class video game publisher. Wester stated in 2013 that many of these games were "terribly bad", but that some proved to be strong performers, such as Mount & Blade.[4] GamersGate eventually was spun off to be its own entity in 2008, while Paradox continued to acquire additional titles to fill its distributor catalogue which helped to finance continued development of grand strategy titles from the Paradox Development Studio. By 2013, the company had reached 100 employees, and established new offices in Stockholm, Sweden.[4]
<br /><br />
Paradox Interactive continued to publish numerous games from smaller developers over the next several years, finding success in games like Magicka from Arrowhead Game Studios (the publisher's first title to break 1 million copies sold) and War of the Roses from Fatshark. Wester and others in Paradox admitted in 2013 that this approach had been ambitious and led to issues with quality control in the resulting games, leading to a general impression about Paradox games being buggy.[3] The publisher had greenlit and invested in several titles by its internal studios without careful review that failed to pan out, such as East vs. West, a spin-off from the Hearts of Iron series, By 2014, the company had made a decision to become much more selective of which titles to publish, making sure they were able to provide the necessary quality control support each title needed before agreeing to publish.[5] Crusader Kings II in 2012 was one of the first games developed and published by Paradox with more attention focused on development timelines and testing to avoid past mistakes, and Paradox has since followed a similar model on its future titles.[6]
One of Paradox Interactive's more notable publishing deals was their agreement with Colossal Order in 2011 to publish their Cities in Motion transportation simulation game and later its sequel Cities in Motion 2.[7] Colossal Order ultimately wanted to produce a city simulation game to challenge Electronic Arts SimCity series but Paradox had expressed concerns regarding the competition. However, after the release of the 2013 SimCity game and the poor reception it received due to a variety of gameplay changes and difficulties with online services required to play the game, Paradox greenlit Colossal Order for its city simulation game, Cities: Skylines,[8][9] which was released in 2015 and has sold over 12 million copies by June 2022.[10]
<br /><br />
Another collaboration for Paradox was with Obsidian Entertainment. Obsidian, having struggled financially, crowd-sourced the development of a new game, Pillars of Eternity, and entered into a publishing deal for it through Paradox.[11] Obsidian published their next title, Tyranny through Paradox.
Paradox opened its second internal development studio, Paradox Arctic, located in Umeå, Sweden, formed from former members of EA DICE and Starbreeze Studios, in 2014.[12]

</p>

            <p>Alongside the 500-series cameras, a series of focal plane shutter cameras was introduced. This 2000-series started with the 2000 FC, and progressed to the 2000 FC/M, 2000 FCW and 2003 FCW. Though much of the 20 years between the discontinuation of the 1000F and the introduction of the 2000 FC was spent designing an improved focal plane shutter, the 2000-series again used corrugated metal foil as material for the shutter curtains, though now titanium replaced the original stainless steel. As before, the metal shutter curtains proved to be quite easily damaged by clumsy fingers, which is why all 2000-series cameras except the 2000FC have a safety feature that retracts the shutter curtains as soon as the magazine is taken off. The 2000-series cameras were replaced by the 200-series cameras (with rubberized cloth shutter curtains), which included the 201 F, 202 FA, 203 FE, and 205 TCC/205 FCC. While the 201 F was a manual control camera, the other three 200-series models added a level of metering and exposure automation to the V series.</p>
            <div>There were also two series of medium format view cameras developed related to the V series: the FlexBody and the ArcBody.</div>

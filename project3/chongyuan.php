            <h4>XPan</h4>
            <div>The dual-format XPan and XPan II were Hasselblad's first cameras to use 35mm film. Built with a rubber-covered titanium and aluminium body, they were designed as a coupled rangefinder camera with interchangeable, compact lenses.</div>
            <p>The XPan cameras are re-branded versions of the Fuji TX-1 and TX-2. The XPan II has every feature of the original, but grants the user the ability to record thirty-minute exposures compared to the old limit of three minutes. Electronic exposure information in the viewfinder is another additional feature of the XPan II.</p>
            <div>The XPan reverted to the focal plane shutter, offering 8–1/1000 s, and flash sync from B (max. 270 s) – 1/125 s.</div>
            <p>The intent in releasing the XPan was to provide medium format image quality on 35mm film. The XPan utilised the entire area of the 35mm film for either panorama or 35mm format, providing a panorama effect without masking the film or reducing image quality. This technique produced a panorama negative almost three times larger than traditional masking and over five times larger than that of APS cameras.</p>
            <div>The XPan is now discontinued.</div>

            <div>In 1984, Victor Hasselblad AB went public, with 42.5% of the company being sold on the Swedish stock exchange. The next year, Swedish corporation Incentive AB bought 58.1% of Hasselblad, and in 1991, they acquired the remainder of the shares, taking VHAB back to being a private corporation.</div>
            <div>In 1985 Hasselblad established the subsidiary, Hasselblad Electronic Imaging AB, to focus on digital imaging and transmission systems.</div>
            <div>In 1991, the 200 series of automated focal plane shutter equipped models was introduced. This was the last major technical development in the course of the classic (now known as "V-System", after Victor) Hasselblad camera.</div>
            <div>In 1996, Hasselblad was sold, with the new owners being UBS, Cinven, and the Hasselblad management.</div>

            <h3>Fuji, Shriro, Imacon, and the digital age</h3>
            <div>In 1998, Hasselblad began selling the XPan, a camera designed and made in Japan by Fujifilm.</div>
            <p>In 2002, they introduced the H-System, retroactively renaming their original camera line the V-System. The H-System marked an essential transition for the company. It dropped the traditional Hasselblad square negative format, instead using 6 × 4.5 cm film and a new series of lenses. The then owners had no confidence in Hasselblad's already advanced digital project returning a profit, and, seeing the relative success in the market of the modern (i.e. fully automated) 645 cameras made by manufacturers like Pentax and Mamiya, closed down Hasselblad's digital department and directed all effort towards making this 645 film camera. The H-System is largely designed and manufactured by Hasselblad, with Fuji's involvement being limited to finalizing Hasselblad's lens designs and producing the glass for the lenses and viewfinders. Fuji was allowed under the agreement to sell the H1 under their name in Japan only.</p>
            <div>In January 2003, the Shriro Group acquired a majority shareholding in Hasselblad. The group had been the distributors for Hasselblad in Japan, Hong Kong, China, Taiwan ROC, Singapore, and Malaysia for over 45 years.</div>
            <p>The following year, in August 2004, Shriro Sweden, the holding company of Victor Hasselblad AB, and Swedish subsidiary of Shriro Group, announced the acquisition of high-end scanner and digital cameraback manufacturer, Imacon (established in 1995 to launch the Flextight scanners). The intent of the move was to correct the mistake the previous owners made when they thought there was no money to be made selling digital products and put a stop to Hasselblad's own advanced digital project, and to renew Hasselblad's ambitions in the professional digital photographic sector.</p>
            <p>The move was perceived as part of an industry-wide move to respond to the trend away from film to digital. Christian Poulsen, chief executive of Hasselblad after the merger (previously founder and CEO of Imacon), said, "They finally realized there was no future. It was impossible to keep Hasselblad alive without digital".</p>
            <p>This has secured their market position, with nearly all of their previous medium format camera competition going through sale (Mamiya), closure (Contax, Bronica, Exakta 66, Kiev), or greatly reduced market presence (Rollei, Pentax—which was also sold to Hoya), and other medium format digital back makers being faced with accordingly restricted markets. Despite this Hasselblad has struggled to turn a profit relative to the market share leader Phase One.</p>
            <div>On 30 June 2011, A German private equity firm Ventizz announced it had acquired a 100% stake in Hasselblad.</div>
            <p>In late 2015, Chinese aerial photography and drone manufacturer DJI acquired a minority interest in Hasselblad. In early January 2017 DJI acquired the majority stake. In July 2018, DJI's Mavic 2 PRO drone was advertised by UK based retail shopping company Argos. This drone was the first to carry a camera featuring the Hasselblad branding.</p>

            <h2>In space</h2>
            <div>Several different models of Hasselblad cameras were taken into space, all specially modified for the task.</div>
            <p>The Hasselblad cameras were selected by NASA because of their interchangeable lenses and magazines. Modifications were made to permit ease of use in cramped conditions while wearing spacesuits, such as the replacement of the reflex mirror with an eye-level finder.</p>


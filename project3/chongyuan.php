            <h4>V System</h4>
            <p>The name "V System" was not created until the development of the "H System"; with a new system premiering, Hasselblad needed a designation to differentiate the older product line. The Hasselblad V-System evolved out of Victor Hasselblad's desire to develop a small camera with fast lenses and shutters, that was as easily hand-holdable as a Leica, but with a larger film format. The Rolleiflex's 6 × 6 format was deemed ideal: large enough to provide high image quality, but small enough to fit inside a compact camera. The Rolleiflex's leaf shutter lacked the fast shutter speeds focal plane shutters could provide, and neither Leica rangefinder nor Rolleiflex TLR provided the(TTL) through the (taking) lens viewing that the slow to use, big Graflex SLR provided. These considerations led to the 1600 F and a flexible camera system that includes interchangeable bodies, lenses, viewfinders, winders, film magazines and holders, and other accessories. Problems with the focal plane shutters in the 1600 F and 1000 F cameras and especially the increasing importance of electronic flash led to the development of the manual leaf shutter-based medium-format 6 × 6 (6 × 6 cm or 2¼ × 2¼ inches) 500 C SLR camera in 1957 which offered flash synchronization at all shutter speeds. The 500 C was joined by the motor-driven 500 EL SLR camera in 1964. Apart from the housing that incorporates the motor drive and the NiCd batteries, this camera is similar in appearance and operation to the Hasselblad 500 C and uses the same magazines, lenses and viewfinders. These two cameras, together with the Superwide Camera (SWC) which was introduced in 1954 as a wide angle camera using the Carl Zeiss Biogon 38 mm f/4.5 lens and built-in levels for exacting architecture photography, formed the core of the V-system and shared most accessories (with a few exceptions).</p>

            <figure>
                <img src="images/283px-Hasselblad_503CW_V96C.jpg" alt="Hasselblad 503">

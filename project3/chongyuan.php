            <p>EL electric cameras were used for the first time on Apollo 8. A heavily modified 500 EL, the so-called Hasselblad Electric Camera (HEC) was used from Apollo 8 on board the spacecraft. Three 500 EL cameras were carried on Apollo 11. An even more extensively modified Hasselblad EL data camera (HDC), equipped with a special Zeiss 5.6/60 mm Biogon lens and film magazines for 150–200 exposures, was used on the Moon surface on the Apollo 11 mission. This command module camera, carried on Apollo 11, was a simplified version of the commercial Hasselblad 500 EL motorized film advance camera. Used for color still photography, it could operate in the command module or in the vacuum of space.</p>
            <p>All following NASA missions also had Hasselblad cameras on board. The photographic equipment and films used on the five subsequent flights were similar to that taken on Apollo 11. On Apollo 15, the 500 mm telelens was added. During the Space Shuttle period cameras based on the 500 EL/M, 553 ELX, 205 TCC and 203 FE have been used.</p>
            <div>There are 11 Hasselblad cameras currently sitting on the lunar surface, where only the film magazines were brought back to Earth.</div>

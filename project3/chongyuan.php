            <h2>Company history</h2>
            <p>The company was established in 1841 in Gothenburg, Sweden, by Fritz Wiktor Hasselblad, as a trading company, F. W. Hasselblad and Co. The founder's son, Arvid Viktor Hasselblad, was interested in photography and started the photographic division of the company. Hasselblad's corporate website quotes him as saying, "I certainly don't think that we will earn much money on this, but at least it will allow us to take pictures for free."</p>
            <p>In 1877, Arvid Hasselblad commissioned the construction of Hasselblad's long-time headquarters building, in use until 2002. While on honeymoon, Arvid Hasselblad met George Eastman, founder of Eastman Kodak. In 1888, Hasselblad became the sole Swedish distributor of Eastman's products. The business was so successful that in 1908, the photographic operations were spun off into their own corporation, Fotografiska AB. Operations included a nationwide network of shops and photo labs. Management of the company eventually passed to Karl Erik Hasselblad, Arvid's son (grandson of founder F. W.). Karl Erik wanted his son, Victor Hasselblad, to have a wide understanding of the camera business, and sent him to Dresden, Germany, then the world center of the optics industry, at age 18 (c. 1924).</p>
            <p>Victor spent the next several years studying and working in various photography related endeavors in Europe and the US, including Rochester, New York, with George Eastman, before returning to work at the family business. Due to disputes within the family, particularly with his father, Victor left the business and in 1937 started his own photo store and lab in Gothenburg, Victor Foto.</p>


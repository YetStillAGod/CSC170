            <h3>World War II</h3>
            <p>During World War II, the Swedish military captured a fully functioning German aerial surveillance camera from a downed German plane. This was probably a Handkammer HK 12.5 cm/7x9, which bore the codename GXN and the military account number Fl.38510.</p>
            <p>The Swedish government realised the strategic advantage of developing an aerial camera for their own use, and in the spring of 1940 approached Victor Hasselblad to help create one. In April 1940, Victor Hasselblad established a camera workshop in Gothenburg called Ross AB in a shed at an automobile shop, working in the evenings in cooperation with a mechanic from the shop and his brother, and began designing the HK7 camera.</p>
            <p>By late 1941, the operation had over 20 employees and the Swedish Air Force asked for another camera, one which would have a larger negative and could be permanently mounted to an aircraft. This model was the SKa4. Between 1941 and 1945, Hasselblad delivered 342 cameras to the Swedish military.</p>
            <div>In 1942, Karl Erik Hasselblad died and Victor took control of the family business. During the war, in addition to the military cameras, Hasselblad produced watch and clock parts, over 95,000 by the war's end.</div>

            <h3>Post-war</h3>
            <div>After the war, watch and clock production continued, and other machine work was also carried out, including producing a slide projector and supplying parts for Saab automobiles.</div>
            <p>The first successful commercial camera for Hasselblad, was the Hasselblad Universal camera, which was made by Szilárd Szabad. This camera was produced in over 1500+ units over a span of a few years, and quickly became the preferred all-around workhorse of the majority of professional Scandinavian photographers. It is perhaps the most important Hasselblad camera made, as it was what got Scandinavian professionals to move away from Kodak and Agfa, which had more or less 90% of the professional market at the time.</p>
            <p>Victor Hasselblad's real ambition was to make high-quality civilian cameras. In 1945–1946, the first design drawings and wooden models were made for a camera to be called the Rossex. An internal design competition was held for elements of the camera; one of the winners was Sixten Sason, the designer of the original Saab bodywork.</p>
            <p>In 1948, the camera later known as the 1600 F was released. The new design was complex, and many small improvements were needed to create a reliable product; the watchmaking background of many of the designers produced a design which was sophisticated, but more delicate than what was permissible for a camera. Only around 50 units were produced in 1949, and perhaps 220 in 1950, of what collectors have now designated the Series One camera. The Series Two versions of the 1600 F, perhaps as many as 3300 made from 1950 to 1953, were more reliable but still subject to frequent repairs, with many units having been cannibalized or modified by the factory. The biggest problem was its shutter, a focal-plane shutter that was hard to keep accurate. Using 120 size film it was formatted to a square 6 × 6 cm or 2+1⁄4 × 2+1⁄4 inches, which meant there was no more need to turn the camera on its side.</p>
            <p>In 1954, they mated the groundbreaking new 38 mm Biogon lens designed by Ludwig Bertele of Zeiss to a shallow non-reflex body to produce the SWA (supreme wide angle, later changed to super wide angle). Though a specialty product not intended to sell in large numbers, the SWA was an impressive achievement, and derivatives were sold for decades. Hasselblad took their two products to the 1954 photokina trade show in Germany, and word began to spread.</p>
            <p>In 1953, a much-improved camera, the 1000 F was released. It too had a focal-plane shutter which led to its final replacement by the 500 C but nonetheless provided a big leg up in the medium format. It had a very fine 250 mm f4 Sonnar sport lens that made it a great wildlife camera. Lenses ranged from a 60 mm Distagon, standard 80 mm Planar, and on up to the 250 mm.</p>
            <p>In December 1954, the 1000 F camera received a rave review from the influential American photography magazine, <em>Modern Photography</em>. They put over 500 rolls of film through their test unit, and intentionally dropped it twice, and it continued to function. But the 1000 F also had shutter problems and finally gave way to the lens-mounted, tried-and-true Compur shutter, retaining its focal-plane shutter/curtain only to mask the film until the Compur shutter closed, then opened to expose the film. This was a far more dependable system, even though it meant having a shutter in each lens.</p>

            <h3>Hasselblad camera comes into its own</h3>
            <p>The real turning point for the company occurred in 1957. The 1000 F was replaced by the 500 C. The landmark 500 C design formed the basis for Hasselblad's product line for the next sixty years, with variants being produced until 2013. It was not until 1960, though, that Hasselblad's cameras became profitable; prior to this point, the company was still being entirely supported by sales of imported photographic supplies, including their distribution of Kodak products.</p>
            <p>In 1962, NASA began to use Hasselblad cameras on space flights, and to request design modifications. The first motor-driven camera, the 500 EL, appeared in 1965 as a result of NASA requests. While Hasselblad had enjoyed a slowly but steadily growing reputation among professional photographers through the 1950s, the publicity created by NASA's use of Hasselblad products dramatically increased name recognition for the brand.</p>
            <div>In 1966, with the increasing success of the camera division, Hasselblad exited the photographic supply and retailing industry, selling Hasselblad Fotografiska AB to Kodak.</div>

            <h3>1970s onward</h3>
            <div>In 1976, Victor Hasselblad sold Hasselblad AB to a Swedish investment company, Säfveån AB. When he died in 1978, he left much of his fortune to the Hasselblad Foundation.</div>
            <p>In 1977, the 2000 series of focal plane shutter equipped models was introduced. The 2000-series cameras had been intended to provide full exposure automation. The 2000 FC however was rushed and introduced without the automated features, partly because of a rethink about the way the automation should be accomplished (electronic vs. electro-mechanical). It was the last new camera produced during Victor Hasselblad's lifetime.</p>


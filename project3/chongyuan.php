            <p>Modifications by NASA technicians were further refined and incorporated into new models by Hasselblad. For example, development of a 70 mm magazine was accelerated to meet the space program.</p>
            <div>The first modified (in fact simplified) Hasselblad 500 C cameras were used on the last two Project Mercury missions in 1962 and 1963. They continued to be used throughout the Gemini spaceflights in 1965 and 1966.</div>

            <h3>Apollo program</h3>
            <div>A general program of reliability and safety was implemented following the Apollo 1 fire in 1967, addressing such issues as reliability and safe operation of electrical equipment in a high-oxygen environment.</div>

            <figure>
                <img src="images/HasselbladUllism.jpg" alt="Hasselblad 500 EL/M">
                <figcaption>Hasselblad 500 EL/M "20 years in space" anniversary edition with 70 mm back, similar to the ones used in the Apollo Program</figcaption>

            <p>Throughout the life of the V Series, Hasselblad incrementally updated the cameras. The 500C gave way to the 500C/M, the 503CX and 503CXi, the 501C and 501CM, and finally the 503CW as the basic manual. The SWC was replaced by the SWC/M, the 903 SWC, and finally by the 905 SWC. The 500 EL's replacements included the 500EL/M, 500ELX, 553 ELX, and the 555ELD. First introduced in the 500ELX, TTL/OTF (through the lens/off the film) flash metering was also a feature of the 503CX, which was replaced by the 503CXi and finally the 503CW. The 503CWD was the very last iteration of the V-series line and was a limited edition variant produced in 2006 to commemorate the centenary of the birth of Victor Hasselblad (born 1906). The 503CWD was supplied with a match-numbered CFV-16 digital back. The entire production run was limited to only 500 units, all numbered.</p>

            <figure>
                <img src="images/Hasselblad_500_CM.jpg" alt="Hasselblad 500 C/M">

            <p>Hasselblad 1000F and especially 1600F cameras are very rare on the secondhand market and usually not in working condition because of age, neglect, and a lack of spare parts and qualified repairmen. Many cameras suffer from corrosion of the chrome rims. A lot of lenses suffer from scratches, fungus, discoloration and separation. Cameras in good condition can therefore fetch fairly high prices.</p>
            <p>The 500C was produced to replace the F-series cameras. It changed the troublesome focal plane shutter for a leaf shutter in each C lens. The camera has continued for over 40 years with only minor improvements. A variation of the 500C was used by NASA for all their Gemini and Apollo missions. Most lenses were made by Zeiss in Germany but the very early 1600F lenses were made by Kodak.</p>


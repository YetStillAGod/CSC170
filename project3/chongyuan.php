            <h4>H System</h4>
            <div>Hasselblad launched the H System at photokina in September 2002.</div>

            <h4>H1</h4>
            <div>The H1 departed from previous Hasselblad cameras in several respects. Hasselblad moved away from the traditional 6 × 6 format to 6 × 4.5 cm, and included autofocus lenses.</div>
            <p>The camera used Fujinon manufactured lenses and prisms, thus departing from Hasselblad's long association with Carl Zeiss when it comes to lens manufacturing. The shutter in the lenses was still manufactured by Hasselblad as well as the body. Hasselblad initially invited both PhaseOne and Kodak to develop digital backs for the H-System.</p>
            <div>The H1 had a number of other innovations, including:</div>
            <ul>
                <li>replacement of the removable dark slide with a fold-out lever</li>
                <li>inserts and backs that could accept both 120 and 220 film</li>
                <li>automatic film advance</li>
                <li>digital back integration</li>
                <li>electronic leaf shutters with timing from 1/800 seconds down to 18 hours</li>
            </ul>
            <div>As with the V-series, most H1- and H2-series components were compatible with one another.</div>

            <h4>H1D</h4>
            <div>Identical to the H1, but sold bundled with a Hasselblad-branded Imacon 22Mp iXpress back which coupled with a 40GB Image Bank allowed shooting of up to 850 images in one session. The camera could only be used with the included digital back. This was Hasselblad's first integrated DSLR.</div>

            <h4>H2</h4>
            <div>Hasselblad has built into the H2 cross-platform camera a new single-battery operation of the camera with the new Ixpress CFH digital back, offering one on/off switch and one operating system, facilitating streamlined, integrated operation. With existing digital backs already in the market, the H2 delivers the exact functionality of the H1. The H2 camera was discontinued in October 2007.</div>

            <h4>H2D</h4>
            <div>2nd generation integrated DSLR. First Hasselblad camera to shoot to new Raw format called the 3FR. Conversions to 3F files could be done in Flexcolor or Phocus. Can utilize a CF card. No longer necessary to shoot to an Image Bank via link cable. Firewire 800 capable. New metering patterns to match the larger sensors. A new lithium ion 1,850 mAh battery was introduced that would power both the H2D body and digital back.</div>

            <h4>H2F</h4>
            <div>The H2F can be used either as a film camera or as a digital camera when mated with a Hasselblad CF31, CF22, CF22 MS, CF39, or CF39 MS back. It is completely identical to the H2, but this camera was created to "lock out" other digital back providers from the H-series platform. The H2F is compatible with all of the H lenses, including the HCD 24mm, HCD 28mm, and the new HCD 35-90mm zoom lens.</div>

            <h4>H3D</h4>
            <div>3rd generation integrated DSLR. The H3D offered software functionality that provided better integration between camera, viewfinder and back than the earlier H1 or H2 could provide. These two early H-System cameras, after all, were not primarily designed as digital cameras, with the H2 carrying the 'legacy' of the H1. Hasselblad's official position on the move was:</div>
            <p><em>"In truth, [the H1] was a great film camera to which a digital back could be fitted, and...Hasselblad started to look at ways that image quality and functionality could be enhanced even more through better integration...The H2 camera has not, in any way, been diminished by Hasselblad's separate development of functions specifically for the integrated H3D. However, lacking the necessary integration of the new camera engine and Hasselblad Flexcolor software, these functions cannot work on the H2."</em></p>

            <h4>H3DII</h4>
            <div>4th generation integrated DSLR. Introduced in 2007, the H3DII systems have a higher level of integration between the camera and the image sensor than stand-alone digital camera backs, but a disadvantage is that film backs are not usable in the H3DII. Improvements of the HD3DII line were:</div>
            <ul>
                <li>larger and improved 3" TFT display</li>
                <li>new heat sink replaces the fan from the original H3D, making the digital back quieter</li>
                <li>drive button is now WB/ISO. Drive functions moved in the Menu</li>
                <li>new ability to use the GIL (Global Image Locator)</li>
            </ul>

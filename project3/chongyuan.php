            <h4>1600F and 1000F</h4>
            <p>Hasselblad's first civilian camera was launched in 1948. Born from the idea of Victor Hasselblad to create the "ideal camera", it was a 6 × 6 cm format focal-plane shutter SLR camera that was six inches long. First simply known as the "Hasselblad Camera" it was later named "1600F" after its highest shutter speed of 1/1600 s and "F" for "focal plane". The camera was revolutionary for the time with its modular design that allowed exchanging lenses, viewfinders and film magazines. The shutter was made of thin stainless steel which was light and durable enough to withstand the high acceleration forces of this fast shutter.</p>

            <figure>
                <img src="images/214px-Hasselblad_1600F.jpg" alt="Hasselblad 1600F">
                <figcaption>Hasselblad 1600F with Kodak Ektar 2.8/80 mm lens</figcaption>
            </figure>

            <p>The 1600F cameras did show a couple of problems (especially the first series) so a number of changes were introduced during the production period that lasted from 1949 to 1953. The 1600F was initially released with the Kodak Ektar 2.8/80 mm and the Ektar 3.5/135 mm lenses. Only prototypes were made of the Ektar 6.3/55 mm and the 5.6/254 mm lenses.</p>
            <p>The successor of the 1600F was the 1000F (1953–1957). The 1000F was named after its reduced shortest shutter speed of 1/1000 s. The 1000F has a different shutter mechanism and proved to be more reliable and robust than its predecessor. During production of the 1600F, Carl Zeiss in Oberkochen had become a supplier of lenses for the 1600F/1000F cameras. Zeiss supplied the lenses Distagon 5.6/60 mm, Tessar 2.8/80 mm, Sonnar 3.5/135, Sonnar 4.0/250 and Sonnar 5.6/250 mm. Towards the end of the 1000 F production period a Dallmeyer 5.6/508 mm lens made by Cook and Perkins, England, was also available, but did not fully cover the full film format.</p>

